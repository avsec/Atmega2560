// Basic memory operators
#include <stdlib.h>

//----------------------------------------------------------------------------------------------------------------------
void* operator new(size_t _size)
{
	return malloc(_size);
}

//----------------------------------------------------------------------------------------------------------------------
void* operator new[](size_t _size)
{
	return malloc(_size);
}

//----------------------------------------------------------------------------------------------------------------------
void * operator new (size_t, void * _ptr)
{
	return _ptr;
}

//----------------------------------------------------------------------------------------------------------------------
void operator delete(void* _ptr)
{
	free(_ptr);
}

//----------------------------------------------------------------------------------------------------------------------
void operator delete[](void* _ptr)
{
	free(_ptr);
}

//----------------------------------------------------------------------------------------------------------------------
extern "C" void __cxa_pure_virtual()
{
	while (1);
}